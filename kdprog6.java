public class kdprog6 {
    public static void main(String[]args){
		int a=5,b=6;
		float c=7.f,d=8.3f;
		char e='F';
		double g=3.14;
		
		System.out.println((float)a); //int ke float//
		System.out.println((double)b);//int ke double//
		System.out.println((int)c); //float ke int//
		System.out.println((double)d); //float ke double//
		System.out.println((int)e); //char ke int//
		System.out.println((float)e);//char ke float//
		System.out.println((double)e);//char ke double//
		System.out.println((int)g); //double ke int//
		System.out.println((float)g); //double ke float//
	}
}	

