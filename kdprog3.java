public class kdprog3{
    public static void main(String[] args){
        // Tahap pendeklarasian Variabel// 
        short ks = 10;
        int ki=2;
        long kl=50000;
        char c= 60; 
        char c1='M'; 
        double x =30.2f;
        float y =30.2f;

        System.out.println("Karakter ="+ c); // penulisan karakter sebagai integer
        System.out.println("Karakter =" + c1);// penulisan katakter sebagai karakter
        System.out.println("Bilangan integer (short) = "+ ks); // menampilkan bilangan bulat dengan tipe data short
        System.out.println("\t(int) = "+ ki); // menampilkan bilangan bulat dengan tipe data integer
        System.out.println("\t(long)= "+ kl); // menampilkan bilangan bulat dengan tipe data long
        System.out.println("Bilangan Real x = "+ x);// menampilkan bilangan real dengan tipe data double
        System.out.println("Bilangan Real y = "+ y);// menampilkan bilangan real dengan tipe data float
    }
}